const languageSelect = document.querySelector("#language");
const toast = document.querySelector("#toast");

function currentCopy() {
  return copy[languageSelect.value] || copy.en;
}

function updateLanguage() {
  const data = currentCopy();
  document.documentElement.lang = languageSelect.value;
  document.documentElement.dir = ["ar"].includes(languageSelect.value) ? "rtl" : "ltr";
  document.querySelectorAll("[data-text]").forEach((node) => {
    const key = node.dataset.text;
    if (data[key]) node.textContent = data[key];
  });
  document.querySelector("#languageSkills").value = data.languageSkills;
  document.querySelector("#availability").value = data.availabilityValue;
}

function showToast(message) {
  toast.textContent = message;
  toast.classList.add("visible");
  window.setTimeout(() => toast.classList.remove("visible"), 2200);
}

languageSelect.addEventListener("change", updateLanguage);
document.querySelector("#stageApplication").addEventListener("click", () => {
  showToast("Application packet staged with selected language, source text, and audit fields.");
});

updateLanguage();
